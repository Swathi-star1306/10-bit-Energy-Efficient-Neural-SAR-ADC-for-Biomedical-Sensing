`timescale 1ns/1ps

module tb_neural_sar_adc;

    //--------------------------------------------------
    // SIGNALS
    //--------------------------------------------------

    reg clk;
    reg rst_n;

    reg [9:0] vin_p;
    reg [9:0] vin_n;

    wire [9:0] digital_out;
    wire conversion_done;
    wire spike_detected;

    //--------------------------------------------------
    // DUT
    //--------------------------------------------------

    neural_sar_adc_top DUT
    (
        .clk(clk),
        .rst_n(rst_n),

        .vin_p(vin_p),
        .vin_n(vin_n),

        .digital_out(digital_out),
        .conversion_done(conversion_done),
        .spike_detected(spike_detected)
    );

    //--------------------------------------------------
    // CLOCK
    //--------------------------------------------------

    initial
    begin

        clk = 0;

        forever #5 clk = ~clk;

    end

    //--------------------------------------------------
    // STIMULUS
    //--------------------------------------------------

    initial
    begin

        rst_n = 0;

        vin_p = 0;
        vin_n = 0;

        #20;

        rst_n = 1;

        //------------------------------------------------
        // CASE 1
        //------------------------------------------------

        #20;

        vin_p = 10'd120;
        vin_n = 10'd100;

        #100;

        //------------------------------------------------
        // CASE 2
        //------------------------------------------------

        vin_p = 10'd850;
        vin_n = 10'd100;

        #100;

        //------------------------------------------------
        // CASE 3
        //------------------------------------------------

        vin_p = 10'd500;
        vin_n = 10'd200;

        #100;

        //------------------------------------------------
        // CASE 4
        //------------------------------------------------

        vin_p = 10'd950;
        vin_n = 10'd150;

        #100;

        $finish;

    end

    //--------------------------------------------------
    // MONITOR
    //--------------------------------------------------

    initial
    begin

        $display("=====================================================");
        $display("TIME\tVIN_P\tVIN_N\tSPIKE\tADC_OUT\tDONE");
        $display("=====================================================");

        $monitor("%0t\t%d\t%d\t%b\t%d\t%b",
                 $time,
                 vin_p,
                 vin_n,
                 spike_detected,
                 digital_out,
                 conversion_done);

    end

endmodule
